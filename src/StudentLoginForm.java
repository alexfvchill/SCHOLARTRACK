    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

    String studentNumber = studentNumberTextField.getText();
    String password = new String(passwordField.getPassword());
    

    if (studentNumber.isEmpty() || password.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill in all fields.");
        return;
    }


    try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/scholartrack_db", "root", "12345");

        String sql = "SELECT * FROM students WHERE student_number = ? AND password = ?";
        PreparedStatement pst = con.prepareStatement(sql);
        pst.setString(1, studentNumber);
        pst.setString(2, password); // Use hashed password here if implemented

        ResultSet rs = pst.executeQuery();

        if (rs.next()) {
            int studentId = rs.getInt("student_id");
            UserSession.setStudentId(studentId);

            Preferences prefs = Preferences.userRoot().node(this.getClass().getName());
    if (rememberMeCheckBox.isSelected()) {
    prefs.put("studentNumber", studentNumber);
    prefs.put("password", password);
    
} else {
    prefs.remove("studentNumber");
    prefs.remove("password");
  
}

            JOptionPane.showMessageDialog(this, "Login successful!");
            StudentDashboard dashboard = new StudentDashboard(studentId);
            dashboard.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Invalid student number or password.");
        }

        con.close();

    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Database error: " + ex.getMessage());
    }

    }//GEN-LAST:event_jButton1ActionPerformed

   private void loadRememberedLogin() {
    Preferences prefs = Preferences.userRoot().node(this.getClass().getName());

    String rememberedStudentNumber = prefs.get("studentNumber", "");
    String rememberedPassword = prefs.get("password", "");
    String rememberedConfirmPassword = prefs.get("confirmPassword", "");

    if (!rememberedStudentNumber.isEmpty()) {
        studentNumberTextField.setText(rememberedStudentNumber);
        passwordField.setText(rememberedPassword);
        rememberMeCheckBox.setSelected(true);
    }
}
    
