    private void btnUploadPicActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUploadPicActionPerformed
         JFileChooser chooser = new JFileChooser();
    chooser.setDialogTitle("Select Profile Picture");
    chooser.setFileSelectionMode(JFileChooser.FILES_ONLY);

    int result = chooser.showOpenDialog(this);
    if (result == JFileChooser.APPROVE_OPTION) {
        File file = chooser.getSelectedFile();
        try {
            // Display in GUI
            ImageIcon imageIcon = new ImageIcon(new ImageIcon(file.getAbsolutePath()).getImage()
                    .getScaledInstance(150, 150, Image.SCALE_SMOOTH));
            lblProfilePic.setIcon(imageIcon);

            // Store in DB
            FileInputStream fis = new FileInputStream(file);
            Connection conn = java.sql.DriverManager.getConnection(
                    "jdbc:mysql://localhost:3306/scholartrack_db", "root", "12345");

            String sql = "UPDATE students SET profile_pic = ? WHERE student_id = ?";
            PreparedStatement pstmt = conn.prepareStatement(sql);
pstmt.setBinaryStream(1, fis, (int) file.length()); // parameter 1 = image
pstmt.setInt(2, studentId);  

            int rows = pstmt.executeUpdate();
            if (rows > 0) {
                JOptionPane.showMessageDialog(null, "Profile picture saved to database!");
            }

            // Close resources after successful use
            pstmt.close();
            conn.close();
            fis.close();
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Failed to load image: " + ex.getMessage());
        }
    }
    
    }//GEN-LAST:event_btnUploadPicActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      // Show confirmation dialog before update
    int confirmed = JOptionPane.showConfirmDialog(
        this, 
        "Are you sure you want to update your profile?", 
        "Confirm Update", 
        JOptionPane.YES_NO_OPTION);

    if (confirmed == JOptionPane.YES_OPTION) {
        String name = StudName.getText();
        String address = addressField.getText();
        String school = schoolcombo.getSelectedItem().toString();
        String email = emailField.getText();
        String password = new String(PasswordFieldNew.getPassword());
        String confirmPassword = new String(confirmPasswordField.getPassword());

        if (!password.equals(confirmPassword)) {
            JOptionPane.showMessageDialog(this, "Passwords do not match!");
            return;
        }

        if (name.isEmpty() || address.isEmpty() || school.equals("Please select your School") || email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill out all required fields.");
            return;
        }

        try {
            Connection conn = java.sql.DriverManager.getConnection(
                "jdbc:mysql://localhost:3306/scholartrack_db", "root", "12345");

            String sql = "UPDATE students SET name = ?, address = ?, school = ?, email = ?, password = ? WHERE student_id = ?";
            PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, name);
            pstmt.setString(2, address);
            pstmt.setString(3, school);
            pstmt.setString(4, email);
            pstmt.setString(5, password);
            pstmt.setInt(6, studentId);

            int rows = pstmt.executeUpdate();
            if (rows > 0) {
                JOptionPane.showMessageDialog(this, "Profile updated successfully!");
            } else {
                JOptionPane.showMessageDialog(this, "No changes made.");
            }

            pstmt.close();
            conn.close();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error updating profile: " + ex.getMessage());
        }
    } else {
        // Optional: user canceled the update
        JOptionPane.showMessageDialog(this, "Profile update cancelled.");
    }


    }//GEN-LAST:event_jButton1ActionPerformed


