    private void loadProfileImage() {
    try {
        Connection conn = java.sql.DriverManager.getConnection("jdbc:mysql://localhost:3306/scholartrack_db", "root", "12345");
        String sql = "SELECT profile_pic FROM students WHERE student_id = ?";
        PreparedStatement pstmt = conn.prepareStatement(sql);
       pstmt.setInt(1, studentId);

        ResultSet rs = pstmt.executeQuery();
        if (rs.next()) {
            byte[] imgBytes = rs.getBytes("profile_pic");
            if (imgBytes != null) {
                ImageIcon icon = new ImageIcon(new ImageIcon(imgBytes).getImage()
    .getScaledInstance(150, 150, Image.SCALE_SMOOTH));
      lblProfilePic.setIcon(icon);
            }
        }

        rs.close();
        pstmt.close();
        conn.close();
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Error loading profile image: " + ex.getMessage());
    }
}
    
    private void loadStudentInfo() {
    try {
        Connection conn = java.sql.DriverManager.getConnection(
            "jdbc:mysql://localhost:3306/scholartrack_db", "root", "12345");

        String sql = "SELECT name, address, school, email, password FROM students WHERE student_id = ?";
        PreparedStatement pstmt = conn.prepareStatement(sql);
        pstmt.setInt(1, studentId);// Replace 1 with the actual logged-in student ID

        ResultSet rs = pstmt.executeQuery();
        if (rs.next()) {
            StudName.setText(rs.getString("name"));
            addressField.setText(rs.getString("address"));
            schoolcombo.setSelectedItem(rs.getString("school"));
            emailField.setText(rs.getString("email"));
            PasswordFieldNew.setText(rs.getString("password"));
            confirmPasswordField.setText(rs.getString("password"));
        }

        rs.close();
        pstmt.close();
        conn.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error loading student info: " + e.getMessage());
    }
}
     
    
